package day18;

public class ForLoopIntro {
    public static void main(String[] args) {

        // count 1 to 5

        for (int i = 1; i <= 5 ; ++i){
            System.out.println(i);
            System.out.println("Happy Birthday");
        }

        for (int count = 10; count > 0; --count){
            System.out.println("counter "+count);
        }
    }
}
